# arithmetic operators 

print("=======first question arithmetic operators=====")
a=5
b=2

print(a+b) # this will return 7 because it will add the two integers together

print(a-b) # this will return 3 because it will subtract b from a

print(a*b) # this will return 10 because it will multiply a and b together

print(a/b) # this will return 2.5 because it will divide a by b

print(a//b) # this will return 2 because it will perform floor division, which means it will return the largest integer less than or equal to the result of the division

print(a%b) # this will return 1 because it will return the remainder of the division of a by b

print(a**b) # this will return 25 because it will raise a to the power of b, which means it will multiply a by itself b times