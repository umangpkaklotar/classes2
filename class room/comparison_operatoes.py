x= 10 
y=20

print("is x greater than y :- ", x>y) # this will return false because 10 is not greater than 20

print("is x less than y :- ", x<y) # this will return true because 10 is less than 20

print("is x equal to y :- ", x==y) # this will return false because 10 is not equal to 20   

print("is x not equal to y :- ", x!=y) # this will return true because 10 is not equal to 20

print("is x greater than or equal to y :- ", x>=y) # this will return false because 10 is not greater than or equal to 20

print("is x less than or equal to y :- ", x<=y) # this will return true because 10 is less than or equal to 20

