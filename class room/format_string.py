#  format_string method is used to format the string by replacing the placeholders with the values provided in the format() method. The placeholders are represented by curly braces {} in the string.
print("=======first question format string method=====")

item1=50
item2=100
# Using format() method
print("The price of item1 is {} and the price of item2 is {}".format(item1, item2))


